func TestMarshalIsValidYAML(t *testing.T) {
	rg := Generate(sampleSLO(), burnrate.Standard())
	out, err := Marshal(rg)
	if err != nil {
		t.Fatalf("Marshal: %v", err)
	}
	// Round-trip back through the YAML parser to prove it is well-formed.
	var back RuleGroups
	if err := yaml.Unmarshal(out, &back); err != nil {
		t.Fatalf("generated rules are not valid YAML: %v\n---\n%s", err, out)
	}
	if len(back.Groups) != 1 {
		t.Errorf("round-tripped %d groups, want 1", len(back.Groups))
	}
	if !strings.HasPrefix(string(out), "# Generated by slo-autopilot") {
		t.Error("missing generated-by header comment")
	}
}

func mustDur(t *testing.T, s string) spec.Duration {
	t.Helper()
	d, err := spec.ParseDuration(s)
	if err != nil {
		t.Fatal(err)
	}
	return d
}
